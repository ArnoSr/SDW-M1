<?php

// While - Tant que
echo "While - Tant que : <br/><br/>";

$nombre_de_lignes = 0;

while ($nombre_de_lignes <= 100)
{
    echo 'Ligne ' . $nombre_de_lignes . '<br />';
    $nombre_de_lignes++;
}

echo "<br/><br/>";




// For
echo "For : <br/><br/>";

for ($nombre_de_lignes = 0; $nombre_de_lignes <= 100; $nombre_de_lignes++)
{
    echo 'Ligne ' . $nombre_de_lignes . '<br />';
}

echo "<br/><br/>";
